function lagrang
%определим отрезок интерпол€ции
a=0.0; b=1.0;
%определим вектор узлов интерпол€ции
x=[0 0.1 0.2 0.3 0.35 0.6 0.7 0.9 0.95 1];
%определим значени€ интерполируемой функции,
%счита€ эти значени€ случайными величинами,
%распределенными по нормальному закону
y=[];
for i=1:length(x)
    y=[y randn];
end
%вводим сетку на отрезке интерпол€ции
xv=a:0.01:b;
%организуем цикл расчета значений
%интерполирующей функции в узлах сетки
for i=1:length(xv)
    yv(i)=lagrange(x,y,xv(i),a,b);
end
%рисуем полином Ћагранжа
plot(x,y,'*',xv,yv);
