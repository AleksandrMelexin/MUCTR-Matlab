%лагранжа в точке xz
function yz=lagrange(x,y,xz,a,b)
L=0;
for i=1:length(x)
    %вычисл€ем числитель и знаменатель
    %дроби в (7)
    numerator=1.0; denumerator=1.0;
    for j=1:length(x)
        if i~=j
            numerator=numerator*(xz-x(j));
            denumerator=denumerator*(x(i)-x(j));
        end
    end
    L=L+(numerator/denumerator)*y(i);
end
yz=L;